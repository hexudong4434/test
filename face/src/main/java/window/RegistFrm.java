    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        FileFrm frm = new FileFrm();

        FileFilter fileFilter = new FileFilter() {
            @Override
            public boolean accept(File f) {
                if(f.isDirectory()) return true;
                if(f.getName().toLowerCase().endsWith(".jpg")||
                        f.getName().toLowerCase().endsWith(".png")||
                        f.getName().toLowerCase().endsWith(".bmp")||
                        f.getName().toLowerCase().endsWith(".jpeg")
                )return true;
                return false;
            }
            @Override
            public String getDescription() {
                return ".jpg/.jpeg/.png/.bmp";
            }
        };
        jFileChooser1.setFileFilter(fileFilter);        //注意：添加文件过滤器 等对文件选择器的修改操作一定要放在显示操作的前面，不然可能会失效
        int returnVal = jFileChooser1.showOpenDialog(frm);

        if(returnVal == JFileChooser.APPROVE_OPTION) {
        //获取文件
        imgFile = jFileChooser1.getSelectedFile();//获取选中的文件


        //修改预览图像

        ImageIcon icon=new ImageIcon(imgFile.getPath());

        image = icon.getImage();    //缓存选择的图片
        //引擎识别一下，是否有人脸
        List<FaceInfo> faceInfo = DetectFace.getFaceInfo(image);
        if(faceInfo==null){     //当检测没有人脸时，设置为标准背景图片
            ImageIcon icon1 = new ImageIcon("F:\\AI酒店\\SDK的test图片\\背景.png");
            icon1.setImage(icon1.getImage().getScaledInstance(jLabel7.getWidth(),jLabel7.getHeight(),Image.SCALE_DEFAULT));
            jLabel7.setIcon(icon1);
            image=null;
            jTextField2.setText("");
            return;
        }
        icon.setImage(icon.getImage().getScaledInstance(jLabel7.getWidth(),jLabel7.getHeight(),Image.SCALE_DEFAULT));
        jLabel7.setIcon(icon);
        jTextField2.setText(imgFile.getName());     //设置文件名框体的内容
}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) throws InterruptedException {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        final CapFrm capFrm = new CapFrm();
        capFrm.setVisible(true);
        capFrm.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                if(!capFrm.isSave&&capFrm.btn_cap.getText().equals("重拍")){    //检测到没有保存
                    int result = JOptionPane.showConfirmDialog(null,"没有保存，是否保存？",
                            "提示",JOptionPane.OK_CANCEL_OPTION);
                    if(result== JOptionPane.OK_OPTION){
                        capFrm.isSave = true;
                    }
                }
                if(capFrm.isSave){
                    //识别出face
                    image = capFrm.getImage();
                    Rect rect =  DetectFace.getFaceLocation(image);

                    //获取Label的尺寸
                    int reImgWidth = jLabel7.getWidth();
                    int reImgHeight = jLabel7.getHeight();
                    if(rect==null){//操作图片前检测是否有人脸
                        image=null;
                        ImageIcon icon=new ImageIcon("F:\\AI酒店\\SDK的test图片\\背景.png");
                        jTextField2.setText("");
                        icon.setImage(icon.getImage().getScaledInstance(reImgWidth,reImgHeight,Image.SCALE_DEFAULT)); //图片适应窗口宽度
                        jLabel7.setIcon(icon);  //显示图像
                        return;
                    }

                    //剪切图片
                    image = ImageTools.cutImg(image,rect);

                    ImageIcon icon=new ImageIcon(image);
                    icon.setImage(image.getScaledInstance(reImgWidth,reImgHeight,Image.SCALE_DEFAULT)); //图片适应窗口宽度
                    jLabel7.setIcon(icon);  //显示图像
                    int count = sqlSession.selectOne("getUserCount");
                    count++;
                    String name="face_"+count+".jpg";
                    jTextField2.setText(name);     //设置文件名框体的内容
                    System.out.println("保存成功！");
                }
                capFrm.webcam.close();
                capFrm.dispose();
            }
        });
    }//GEN-LAST:event_jButton3ActionPerformed

