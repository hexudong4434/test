    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, IOException {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:    提交按键

        String warn = "";
        int f = 0;

        if("".equals(jTextPane1.getText().trim())||jTextPane1.getText()==null){
            f+=1;
        }
        if(image==null){
            f+=2;
        }
        if(f!=0){       //如果输入结果异常，则清空并退出
            if(f==1) {
                jTextPane1.setText("");
                warn= "请输入有效的名字";
                }
            if(f==2){
                warn="请选择或者拍摄一张含有人脸的标准图片";
            }
            if(f==3){
                jTextPane1.setText("");
                warn="请输入姓名，并且上传一张有效的人脸图像";
            }
            JOptionPane.showMessageDialog(null,warn);
            return;
        }
        //如果无异常则提交数据库，并且提示成功并退出
        String path="F:\\AI酒店\\SDK的test图片\\系统人脸图片";

        //写入图片到本地路径
        imgName = jTextField2.getText();

        if(image instanceof ToolkitImage)       //注意：用文件选择器打开的文件格式 为 ToolkitImage
        image = ((ToolkitImage)image).getBufferedImage();
        ImageIO.write((BufferedImage) image,"jpg",new File(path+"\\"+imgName));

        user = new User();
        user.setUser_name(jTextPane1.getText());    //获去用户名
        if(jRadioButton1.isSelected()) user.setGender("男"); //获取性别
        else user.setGender("女");
        String phone = jTextField1.getText();
        user.setPhone(phone);   //电话
        String faceFeature="";
        //获取人脸二进制数据 ,以字符串转存
        FaceFeature feature = DetectFace.detectFace(image);//获取脸部特征数据
        faceData = feature.getFeatureData();

        for(byte a : faceData){
            faceFeature = faceFeature + a + ".";
        }
        user.setFace_feature(faceFeature);
        String pathTemp = path;
        user.setImg_name(imgName);  //文件名
        user.setImg_path(path);     //路径
        int rs =  sqlSession.insert("addUser",user);
        sqlSession.commit();
        if(rs>0){
            System.out.println("提交成功");
            DetectFace.exitEngine();    //关闭识别引擎
            this.dispose();     //成功之后关闭 窗口

        }

    }//GEN-LAST:event_jButton1ActionPerformed

